print(9/4)      # 除法
print(9//4)     # 整除
print(3**4)     # 次方
print(9%4)      # 餘數
print(-9//4)    # 負數的除法跟其他語言不同，是向負無窮取值
print(-9%4)

print(2+3)
print(int("2")+3)

a = 40
print("a = ", a)
print("a>>2 = ", a>>2)  # 右移
print("a<<2 = ", a<<2)  # 左移
print("a>>3 = ", a>>3)

x = 10
y = 10
print("x is y : ", x is y)   # is比較兩個變數是否是同一個物件

x = (10, 20, 30)
y = 10
print("y in x : ", y in x)  # in判斷一個物件是否屬於另一個物件(跟RDB雷同)